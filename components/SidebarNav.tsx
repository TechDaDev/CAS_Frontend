export function SidebarNav() {
  const { user } = useAuth();
  const pathname = usePathname();
  const isPlatformRoute = pathname?.startsWith('/platform');
  const isSuperuser = user?.is_superuser;
  const [institution, setInstitution] = useState<Institution | null>(null);

  // Load institution data for logo display
  useEffect(() => {
    const loadInstitution = async () => {
      if (user?.institution_id && !isPlatformRoute) {
        try {
          const data = await institutionsService.getInstitution(user.institution_id);
          setInstitution(data);
        } catch (err) {
          console.error('Failed to load institution:', err);
        }
      }
    };

    loadInstitution();
  }, [user?.institution_id, isPlatformRoute]);

  // Determine which navigation items to show
  let navItemsToShow: NavItem[] = [];
  
  if (isPlatformRoute && isSuperuser) {
    // Show platform navigation for super admins on platform routes
    navItemsToShow = platformNavItems;
  } else {
    // Show institution navigation
    navItemsToShow = institutionNavItems;
    
    // If superuser is on institution routes, also show platform link
    if (isSuperuser && !isPlatformRoute) {
      navItemsToShow = [
        ...institutionNavItems,
        {
          label: 'إدارة المنصة',
          href: '/platform',
          icon: (
            <svg className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M10.325 4.317c.426-1.756 2.924-1.756 3.35 0a1.724 1.724 0 002.573 1.066c1.543-.94 3.31.826 2.37 2.37a1.724 1.724 0 001.065 2.572c1.756.426 1.756 2.924 0 3.35a1.724 1.724 0 00-1.066 2.573c.94 1.543-.826 3.31-2.37 2.37a1.724 1.724 0 00-2.572 1.065c-.426 1.756-2.924 1.756-3.35 0a1.724 1.724 0 00-2.573-1.066c-1.543.94-3.31-.826-2.37-2.37a1.724 1.724 0 00-1.065-2.572c-1.756-.426-1.756-2.924 0-3.35a1.724 1.724 0 001.066-2.573c-.94-1.543.826-3.31 2.37-2.37.996.608 2.296.07 2.572-1.065z" />
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15 12a3 3 0 11-6 0 3 3 0 016 0z" />
            </svg>
          ),
          requiresSuperuser: true,
        },
      ];
    }
  }

  const filteredNavItems = navItemsToShow.filter(item => {
    if (item.requiresSuperuser && !user?.is_superuser) {
      return false;
    }
    return true;
  });

  return (
    <div className="flex h-full flex-col">
      {/* Institution Logo Header */}
      {!isPlatformRoute && user?.institution_id && (
        <div className="border-b border-slate-200 px-4 py-4">
          <div className="flex items-center gap-3">
            <div className="h-10 w-10 rounded-lg bg-slate-100 flex items-center justify-center overflow-hidden">
              {institution?.logo ? (
                <img src={institution.logo} alt={institution.name} className="h-full w-full object-cover" />
              ) : (
                <span className="text-lg font-bold text-slate-400">
                  {institution?.name?.[0] || user?.institution_name?.[0] || 'C'}
                </span>
              )}
            </div>
            <div className="flex-1 min-w-0">
              <p className="text-sm font-medium text-slate-900 truncate">
                {institution?.name || user?.institution_name || 'الكلية'}
              </p>
              <p className="text-xs text-slate-500">{user?.is_superuser ? 'مدير المنصة' : 'موظف'}</p>
            </div>
          </div>
        </div>
      )}
      
      <nav className="flex-1 space-y-1 px-3 py-4">
        {filteredNavItems.map((item) => {
          const isActive = pathname?.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`group flex items-center rounded-md px-3 py-2 text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-blue-50 text-blue-700'
                  : 'text-slate-700 hover:bg-slate-100 hover:text-slate-900'
              }`}
            >
              <span className={`ml-3 flex-shrink-0 ${isActive ? 'text-blue-600' : 'text-slate-400 group-hover:text-slate-600'}`}>
                {item.icon}
              </span>
              {item.label}
            </Link>
          );
        })}
      </nav>
    </div>
  );
}
